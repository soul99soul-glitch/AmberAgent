        private fun blobFileFor(sampleName: String): File = File(CORPUS_DIR, "$sampleName.pmda")

        @JvmStatic
        @ParameterizedRobolectricTestRunner.Parameters(name = "{0}")
        fun samples(): List<Array<Any>> =
            CORPUS_DIR.listFiles { f -> f.extension == "md" }
                .orEmpty()
                // Only samples that have a paired golden blob can be compared tree-vs-tree.
                .filter { File(CORPUS_DIR, "${it.nameWithoutExtension}.pmda").exists() }
                .sortedBy { it.name }
                .map { arrayOf(it.nameWithoutExtension, it) }

        /**
         * ACCEPTED divergence #1 (image alt brackets). The dump renders image alt text as
         * `image=<contentDescription>`. The JVM tree's image alt is the RAW LINK_TEXT including the
         * surrounding `[ ]` (NativeMdTree.kt `linkLabel` KDoc: "the image-alt call site uses the
         * label as-is … native alt text lacks the brackets the JVM alt carries"). The bracket
         * presence is non-visible accessibility metadata, so we strip ONE leading `[` and ONE
         * trailing `]` from the alt segment of `image=` lines on both sides before comparison.
         */
        private fun String.normalizeImageAltBrackets(): String {
            val marker = "image="
            val idx = indexOf(marker)
            if (idx < 0) return this
            val prefix = substring(0, idx + marker.length)
            var alt = substring(idx + marker.length)
            if (alt.startsWith("[")) alt = alt.substring(1)
            if (alt.endsWith("]")) alt = alt.substring(0, alt.length - 1)
            return prefix + alt
        }

        /** Unified-ish diff between the two engines plus the first differing line. */
        private fun buildParityDiff(name: String, jvm: String, native: String): String {
            val j = jvm.lines()
            val n = native.lines()
            val firstDiff = (0 until maxOf(j.size, n.size)).firstOrNull {
                j.getOrNull(it) != n.getOrNull(it)
            }
            val pointer = if (firstDiff != null) {
                "\nfirst diff at line ${firstDiff + 1}:\n" +
                    "  jvm:    ${j.getOrNull(firstDiff)}\n" +
                    "  native: ${n.getOrNull(firstDiff)}"
            } else {
                ""
            }
            return buildString {
                appendLine("dual-tree render parity mismatch for sample '$name'")
                appendLine("(JVM JetBrains tree vs native packed-AST tree, same renderer, same raw input)")
                appendLine("If this is one of the two DOCUMENTED renderer bugs (native heading-drop /")
                appendLine("native inline-code-backtick), add '$name' to KNOWN_DIVERGENT_SAMPLES with a")
                appendLine("citation. If it is a NEW divergence, investigate the root cause first.")
                appendLine(pointer)
                appendLine("----- JVM TREE DUMP -----")
                appendLine(jvm)
                appendLine("----- NATIVE TREE DUMP -----")
                appendLine(native)
            }
        }
