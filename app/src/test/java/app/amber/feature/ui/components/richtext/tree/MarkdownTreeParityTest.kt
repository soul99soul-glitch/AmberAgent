/**
 * TD.Rust.1a Phase 3-B — the dual-render PARITY rig. Renders the SAME markdown TWICE through the
 * SAME production renderer — once driven by the JetBrains JVM tree ([JvmMdNode]), once by the
 * packed-AST native tree ([NativeMdNode]) — and asserts the two semantics dumps match. The renderer
 * is the constant; the tree engine is the only variable. This is the final proof of whether the
 * Stage-4 tree switch is render-safe.
 *
 * ## What it proves (and how it differs from the sibling tests)
 *  - [NativeMdTreeTest] pins the native tree's *accessors* against the mapping doc.
 *  - [MarkdownRendererSnapshotTest] pins the JVM render output against a golden text dump.
 *  - THIS test compares JVM-tree render vs native-tree render of identical input. Equal dumps for a
 *    sample ⇒ the renderer is engine-agnostic for that input. Unequal dumps ⇒ a real renderer
 *    divergence the `markdownAst` flag must not ship over.
 *
 * ## Seam chosen: [MarkdownTreeForParityTest] — render a pre-built result through MarkdownBlock's
 * non-streaming dispatch
 * A Compose test rule permits exactly ONE `setContent`, so the two trees must render in the SAME
 * composition. They cannot both go through `MarkdownBlock(content)` because that entry re-derives the
 * tree from the shared parse cache (one key per content), so both calls would get the same tree.
 * Markdown.kt instead exposes [MarkdownTreeForParityTest], a thin `@VisibleForTesting` composable
 * that runs the EXACT render dispatch `MarkdownBlock` reaches on a settled (non-streaming) result —
 * the `else` arm walking `result.tree.children` via the private `MarkdownNode`, or the identical
 * `MarkdownNew(content)` HTML arm. For a settled result every streaming CompositionLocal already
 * equals its default, so this is render-equivalent to `MarkdownBlock(content)`. Both engines run this
 * ONE composable; the ONLY variable is which [MdNode] tree the result carries. We render both
 * side-by-side under distinct `testTag`s and dump each subtree separately.
 *
 * ## Input alignment: tree-vs-tree on identical RAW text
 * The golden `.pmda` blobs were generated by `dump_corpus` from the RAW `.md` text (no preprocessing
 * — see [NativeMdTreeTest] KDoc). The production funnel parses PREPROCESSED text (`preProcess()` =
 * KaTeX delimiter conversion + bare-URL linkify), but preprocessing is UPSTREAM of the parse and
 * applies EQUALLY to both engines at runtime — it does not distinguish JVM from native. So for a
 * like-for-like tree comparison both paths are fed the same RAW text:
 *  - JVM tree: [parseRawMarkdownForParityTest] runs the JetBrains parse on raw text, bypassing
 *    `preProcess` exactly as the native blob did.
 *  - Native tree: [PackedAstReader] over the golden blob + [nativeMdTreeOrNull] against raw text.
 * This is tree-vs-tree parity on identical input; production preprocessing is engine-neutral.
 *
 * ## ACCEPTED divergence — narrow, cited normalization (controller ruling)
 *  - **Image alt brackets** — the JVM `linkLabel` includes the surrounding `[ ]`; the native
 *    concatenated label does not (NativeMdTree.kt `linkLabel` KDoc + spec review). Image alt surfaces
 *    in the dump as `image=<contentDescription>`, so [normalizeImageAltBrackets] strips a single
 *    leading `[` / trailing `]` from `image=` segments on both sides before comparison. This is the
 *    ONLY normalization applied — never blanket fuzzy matching.
 *  - **`[X]` uppercase task checkbox** — native = checked, JVM = unchecked (controller-accepted GFM
 *    fix; NativeMdTree.kt `taskChecked` KDoc). The semantics dump does NOT surface checkbox state
 *    (see [dumpNormalized] "does NOT capture" list), so this divergence cannot perturb the dumps and
 *    needs no normalization.
 *
 * ## ✅ The two renderer bugs this rig discovered are now FIXED (T14.5 / td-rust-1a)
 * The rig originally surfaced two genuine bugs in the production renderer's tree walk. Both are now
 * fixed with SHAPE-AGNOSTIC renderer dispatch (Markdown.kt) — the trees stay honest to their parsers:
 *
 *  - **BUG #1 (FIXED) — native headings render EMPTY.** The Heading arm of `MarkdownNode` rendered
 *    ONLY children of type [MdNodeType.Paragraph] — the JetBrains `ATX_CONTENT → Paragraph` wrapper.
 *    The native tree has NO wrapper; its heading children are FLAT inline nodes, so the filter matched
 *    nothing. FIX: render the Paragraph wrapper child when present (JVM shape), else render the
 *    heading node itself through `Paragraph()` (native shape — `Paragraph` walks the flat inline
 *    children via `appendMarkdownNodeContent`). JVM golden output is byte-identical (one Paragraph
 *    child per heading → exactly one render either way; snapshot test stays 32/32).
 *  - **BUG #2 (FIXED) — native inline code keeps its backticks.** The generic leaf arm
 *    (`node.children.isEmpty()`) of `appendMarkdownNodeContent` PRECEDED the type-specific arms, so a
 *    childless native `InlineCode`/`MathInline` leaf hit it and was appended raw (backticks /
 *    `$formula$`) instead of dispatching by type. FIX: hoist the `InlineCode` + `MathInline` arms
 *    ABOVE the leaf arm (type-dispatch before shape-dispatch). Audit of every children-shape-gated
 *    arm: only those two inline types are childless in the native tree AND have an arm after the leaf
 *    arm; the Emphasis/Strong/Strikethrough/Link arms are inline CONTAINERS that always carry content
 *    children, so they never hit the leaf arm. JVM byte-identical (those JVM nodes carry marker-token
 *    children and never matched the leaf arm).
 *
 * ## ⚠ Residual classes UNMASKED once #1/#2 were fixed (DONE_WITH_CONCERNS — documented, skipped)
 * Fixing #1/#2 promoted 9 samples to hard parity and uncovered further divergence classes the two bugs
 * had been masking. Class [A] (heading inline-token trim-space) was a renderer-fixable quirk and is now
 * RESOLVED (deliberate behavior change + golden regen); fixing it in turn unmasked two more residuals
 * ([F] ordered-list start number, [G] task-list `[x]` marker text) on samples that had been tagged [A]
 * only — both of which are ALSO renderer-shape-dispatch bugs and are now RESOLVED too (see [F]/[G]
 * below). The remaining classes are tree-/parser-level differences NOT fixable by renderer dispatch
 * without rewriting the native parser. They stay in [KNOWN_DIVERGENT_SAMPLES], skipped and individually
 * cited. The rot guard still re-promotes any that later reach parity.
 *
 *  - **[A] Heading inline-token trim-space (FIXED — parity class A resolved).** The JVM heading path
 *    renders inline content via `Paragraph(trim=true)`; the JetBrains tree splits heading text into
 *    multiple TEXT tokens at punctuation (em-dash / `(` / `"` / `:` / `.` / `'`), and each token was
 *    `.trim()`'d in the leaf arm, dropping the interior spaces. `## GFM Tables — Simple` rendered as
 *    JVM `Tables—Simple`, native `Tables — Simple`. FIX (Markdown.kt): the leaf arm no longer trims
 *    per token; the WHOLE assembled heading content is trimmed ONCE at its outer boundaries via
 *    `AnnotatedString.trimEnds()` in `Paragraph` (gated on `trim`, which only the heading path passes).
 *    Interior token-boundary spaces survive while leading/trailing heading whitespace is still removed
 *    (`## foo ` → `foo`). The JVM goldens were intentionally REGENERATED (whitespace-only restorations
 *    inside heading lines) as part of this deliberate behavior change. Samples that diverged SOLELY on
 *    [A] now render at hard parity and have left [KNOWN_DIVERGENT_SAMPLES]: 11, 12, 17, 18, 25, 31.
 *    The MarkdownEdgeShapeRenderTest `headingPreservesInteriorSpacesAroundPunctuation` /
 *    `headingTrimsOuterWhitespaceButKeepsInterior` guards pin both halves.
 *  - **[B] Backslash-escape resolution (FIXED — parity class B resolved).** The JetBrains (JVM) tree
 *    keeps a CommonMark backslash escape (`\_` / `\!` / `\*`) LITERALLY in its Text-leaf source slice
 *    (`textIn` is a raw `substring`), so escapes rendered as `\!`; the native (pulldown) tree slices
 *    the escape away (its Text node spans only the punctuation). Per CommonMark §2.4 a `\` before any
 *    ASCII-punctuation char escapes it, so the rendered text must show just the punctuation — the JVM
 *    was the wrong side. FIX (Markdown.kt): `resolveBackslashEscapes()` is applied in BOTH leaf-text
 *    render paths — the AnnotatedString leaf arm of `appendMarkdownNodeContent` (grouped-paragraph
 *    path) AND the standalone-`Text` `MdNodeType.Text` arm of `MarkdownNode` (the per-child FlowRow
 *    path a paragraph takes when it contains an Image/MathBlock, e.g. sample 32's escapable run, whose
 *    `\( \)`/`\[ \\ \]` preprocess into inline/block math). The rule replaces `\X`→`X` only for X in
 *    the CommonMark escapable set; a `\` before a non-punctuation char (`back\slash`, `\a \z \1`) or a
 *    trailing `\` stays literal, and InlineCode/MathInline/CodeBlock slice their own raw source (their
 *    arms are hoisted ABOVE the leaf/Text arm), so code/math escapes stay raw. Applying the SAME rule
 *    to the SHARED leaf paths is idempotent on the native tree (its `!` has no backslash to strip).
 *    The JVM goldens were intentionally REGENERATED (backslash removals before escapable punctuation,
 *    samples 03 + 32 only). Sample 03 (whose ONLY divergence was the `middle\_of\_a\_word` escape) is
 *    promoted to hard parity and has left [KNOWN_DIVERGENT_SAMPLES]; sample 32's `\! \#…` escape lines
 *    now MATCH both trees — it stays skipped only on a residual [D] link/href pair the [B] divergence
 *    was masking (parity-rig first-diff 2026-06-12: 2 differing lines, both `links=`). The
 *    MarkdownEdgeShapeRenderTest `escapedPunctuationRendersLiterallyWithoutEmphasis` /
 *    `backslashBeforeNonPunctuationStaysLiteral` / `escapeInsideInlineCodeStaysRaw` guards pin the
 *    fix and its two scope boundaries.
 *  - **[B-tilde] Single-tilde strikethrough delimiter (parser-level; NOT backslash-escape, NOT fixed
 *    by the [B] escape fix).** Source `~this is not struck~`: JVM keeps the literal `~`, native strips
 *    it as a strikethrough delimiter. A parser delimiter disagreement in the renderer's INPUT (sample
 *    23), unrelated to backslash escapes; stays documented.
 *  - **[C] List-item inline-run grouping (FIXED — T14.6).** A TIGHT list item's inline content
 *    (`- Call `code` to…`) is a single grouped Text run on the JVM tree (a PARAGRAPH wrapper child)
 *    but FLAT inline children on the native tree (no Paragraph wrapper — same structural family as
 *    the old BUG #1, but in `ListItemNode`'s per-child loop). The old per-child loop rendered `Call`
 *    / `code` / `to…` as separate Text composables on the native shape. FIX (Markdown.kt): in
 *    `ListItemNode`, dispatch on shape — if the item's direct content contains a Paragraph wrapper
 *    (JVM) render per-child as before; otherwise (native flat inline) group the whole inline run
 *    through ONE `Paragraph()` via the render-time `InlineRunMdNode` view (children = the real flat
 *    inline nodes, nested lists excluded). JVM is byte-identical: the JVM branch is the unchanged
 *    original loop, and the snapshot suite (JVM-tree path) stays 32/32. Samples 10 + 13 promoted to
 *    hard parity; sample 24's [C] component is resolved too (it stays skipped only on the distinct
 *    [B-CJK] strong-emphasis class below).
 *  - **[B-CJK] CJK-flanked strong emphasis (parser-level; unmasked when [C] was fixed in 24).**
 *    Source `常被称为**"离线优先"**策略`: the JVM (JetBrains) tree forms a Strong node (renders bold,
 *    `**` stripped); the native (pulldown-cmark) tree does NOT match the `**` delimiter run when both
 *    flanks are CJK — CommonMark's left/right-flanking rules treat CJK as non-punctuation, so the run
 *    is simultaneously left- and right-flanking and fails to open/close — and emits literal `*` Text
 *    leaves instead. Verified at the TREE level (TREEDIAG): a Strong child on JVM vs bare `*` Text on
 *    native. Like [B], this is a parser disagreement in the renderer's INPUT, not a shape bug.
 *  - **[D] Link / autolink / footnote / reference text.** Email autolink `<x>` (JVM keeps the
 *    brackets, native resolves a mailto link), reference links `[t][ref]` (JVM literal, native
 *    resolved), footnote definitions `[^id]:` (JVM keeps the marker text). Tree-structure differences.
 *  - **[E] Soft-break leading whitespace.** A blockquote soft-break continuation line carries a
 *    leading space on the JVM tree, none on the native tree. Parser whitespace tokenization. Also
 *    surfaces in a top-level paragraph (sample 19) where the JVM tree additionally splits the sentence
 *    into many Text runs at punctuation (`(`/`)`/`:`) and soft-breaks while the native tree groups the
 *    whole sentence into ONE Text leaf — the paragraph analog of the list-item [C] grouping, tagged
 *    [C]+[E] on that sample.
 *  - **[F] Ordered-list start number (FIXED — parity class F resolved).** A list `7. … 8. …` renders
 *    with the SOURCE start number on the JVM tree, which slices each item's literal LIST_NUMBER token
 *    (`7.`, `8.`, …); the native tree has NO literal marker child, so the renderer's old `?: "$index. "`
 *    fallback RENUMBERED from `1.` (`1. 2. 3.` for an authored `7. 8. 9.`). FIX (Markdown.kt
 *    `OrderedListNode`): when the literal LIST_NUMBER marker is ABSENT (native shape) derive the number
 *    from the list's `listStart` accessor — `(listStart ?: 1) + itemIndex` — which both trees decode to
 *    the first item's number (`listStart == 7L` on sample 14, both trees). JVM is byte-identical: the
 *    Unknown marker is always present on the JVM tree, so the literal slice still wins and the computed
 *    branch is native-only by construction. `start=1` / nested ordered lists have `listStart == 1` and
 *    still render `1. 2. 3.`. Sample 14 promoted to hard parity; the
 *    MarkdownEdgeShapeRenderTest `nativeOrderedListStartRendersSourceNumbers` guard pins it.
 *  - **[G] Task-list `[x]` marker text (FIXED — parity class G resolved).** Source `- [x] Increment …`:
 *    the JVM tree relocates the `[x]` into a TaskListMarker node OUTSIDE the Paragraph wrapper (rendered
 *    as a checkbox composable, marker stripped from the text run); the native tree emits the checkbox as
 *    a CHILDLESS TaskListMarker LEAF flat among the item's inline children, and the class-[C] inline-run
 *    grouping fed that leaf into Paragraph where its raw `[x]` text surfaced (`[x]Increment …`). FIX
 *    (Markdown.kt `ListItemNode`, native flat-inline branch): render any TaskListMarker child as the
 *    checkbox via `MarkdownNode` (exactly as the JVM per-child loop does) and EXCLUDE it from the
 *    grouped inline run, so the `[x]` text never reaches Paragraph on either shape. The `[X]` uppercase
 *    checkbox-STATE divergence stays accepted (invisible to the dump — see ACCEPTED divergence above);
 *    only the marker TEXT is fixed here. JVM is byte-identical: its task items carry a Paragraph wrapper
 *    and take the per-child branch, so the fixed branch is native-only for task items by construction.
 *    Sample 15 promoted to hard parity; sample 29's [G] component is resolved too (it stays skipped only
 *    on the distinct [D] footnote-definition class below). The MarkdownEdgeShapeRenderTest
 *    `nativeTaskItemRendersCheckboxAndExcludesMarkerText` guard pins it.
 *
 * The residuals that REMAIN live in the SHARED renderer's INPUT (the tree), not its shape-dispatch, so
 * the `markdownAst` flag must stay default-false until [D]/[E] (and the parser-level [B-tilde] /
 * [B-CJK]) are resolved. Classes [A] (heading trim-space), [B] (backslash-escape resolution), [C]
 * (list-item inline-run grouping), [F] (ordered-list start number) and [G] (task-list `[x]` marker
 * text) are now FIXED (see above) — all renderer-side, shape-agnostic or shared-leaf concerns. See the
 * T14.5 handoff parity report for the full breakdown.
 *
 * Robolectric scaffolding (config / theme / locals / async-load handler) mirrors
 * [MarkdownRendererSnapshotTest] verbatim — a parity gap is attributable to the tree, not the harness.
 */
@RunWith(ParameterizedRobolectricTestRunner::class)
@GraphicsMode(GraphicsMode.Mode.NATIVE)
@Config(sdk = [34], application = Application::class)
class MarkdownTreeParityTest(
    private val sampleName: String,
    private val sampleFile: File,
) {

    // Same async-load swallow as the snapshot test: the QuickJS/Prism highlighter loads a native
    // `.so` absent on the host JVM, so HighlightText's off-thread highlight() resumes with an
    // UnsatisfiedLinkError (possibly wrapped). The dumped code Text is the raw source either way, so
    // we swallow it in the compose effect context rather than crash waitForIdle().
    private val swallowAsyncLoadFailures = CoroutineExceptionHandler { _, t ->
        if (generateSequence<Throwable>(t) { it.cause }.none { it is UnsatisfiedLinkError }) {
            throw t
        }
    }

    @get:Rule
    val compose = createComposeRule(swallowAsyncLoadFailures)

    @Test
    fun nativeTreeRendersEquivalentlyToJvmTree() {
        val rawText = sampleFile.readText()
        val blob = blobFileFor(sampleName).readBytes()

        // ── JVM tree over RAW text (JetBrains parse, preProcess bypassed) ──
        val jvmResult = parseRawMarkdownForParityTest(rawText)

        // ── Native tree over the SAME RAW text, from the golden blob (no JNI on host) ──
        val reader = PackedAstReader(blob)
        assertTrue("$sampleName: golden blob invalid — regen corpus", reader.isValid && reader.validate())
        val nativeRoot = nativeMdTreeOrNull(reader, rawText)
            ?: throw AssertionError("$sampleName: native tree had no decodable root")
        val nativeResult = MarkdownParseResult(
            preprocessed = rawText,
            tree = nativeRoot,
            hasHtmlBlocks = reader.hasHtmlBlocks,
        )

        // hasHtmlBlocks MUST agree: if it didn't, one engine would route to MarkdownNew (HTML
        // re-parse) and the other to the tree path — a structural divergence, not a render nit.
        // The blob-header bit and JVM containsHtmlBlocks() are designed to match (Markdown.kt
        // parsePreprocessedMarkdownUncached KDoc). Assert it so a regression surfaces loudly.
        assertEquals(
            "$sampleName: hasHtmlBlocks disagree (JVM=${jvmResult.hasHtmlBlocks} " +
                "native=${nativeResult.hasHtmlBlocks}) — would split the render path",
            jvmResult.hasHtmlBlocks,
            nativeResult.hasHtmlBlocks,
        )

        // Render BOTH engines in one composition (a Compose test rule allows only one setContent),
        // each under a distinct testTag so we can dump the two subtrees independently.
        val highlighter = Highlighter(RuntimeEnvironment.getApplication())
        compose.setContent {
            CompositionLocalProvider(
                LocalSettings provides Settings(),
                LocalNavController provides Navigator(mutableListOf()),
                LocalHighlighter provides highlighter,
            ) {
                MaterialTheme {
                    Column {
                        Box(Modifier.testTag(TAG_JVM)) {
                            MarkdownTreeForParityTest(content = rawText, result = jvmResult)
                        }
                        Box(Modifier.testTag(TAG_NATIVE)) {
                            MarkdownTreeForParityTest(content = rawText, result = nativeResult)
                        }
                    }
                }
            }
        }
        compose.waitForIdle()

        // dumpNormalized ignores the TestTag property (it captures only Text/ContentDescription/
        // Role/Image/links), so the tagged Box contributes no dump line — only its rendered subtree.
        val jvmDump = compose.onNodeWithTag(TAG_JVM).fetchSemanticsNode().dumpNormalized()
        val nativeDump = compose.onNodeWithTag(TAG_NATIVE).fetchSemanticsNode().dumpNormalized()

        val jvmNorm = jvmDump.normalizeImageAlt()
        val nativeNorm = nativeDump.normalizeImageAlt()
        val parity = jvmNorm == nativeNorm

        if (sampleName in KNOWN_DIVERGENT_SAMPLES) {
            // Rot guard: a known-divergent sample that now MATCHES means a renderer fix landed —
            // promote it back to a hard-parity sample so the proof tightens.
            if (parity) {
                throw AssertionError(
                    "$sampleName now renders at parity — remove it from KNOWN_DIVERGENT_SAMPLES " +
                        "(its documented residual divergence class [D]/[E] / [B-tilde] / [B-CJK] " +
                        "appears fixed).",
                )
            }
            // Still divergent as documented — skip loudly (visible as SKIPPED, never a false PASS).
            assumeTrue(
                "$sampleName diverges due to a documented residual tree-/parser-level class " +
                    "([D] link-footnote text / [E] soft-break whitespace / [B-tilde] single-tilde " +
                    "strike / [B-CJK] CJK-flanked strong — see class KDoc + parity report). BUG #1 " +
                    "(heading-drop), BUG #2 (inline-code-backtick), class [A] (heading trim-space), " +
                    "class [B] (backslash-escape resolution), class [C] (inline-run grouping), class " +
                    "[F] (ordered-list start number) and class [G] (task-list `[x]` marker text) are " +
                    "FIXED; these residuals must be resolved before markdownAst can flip on.",
                false,
            )
        }

        // Non-divergent sample: assert HARD parity.
        if (!parity) {
            throw AssertionError(buildParityDiff(sampleName, jvmNorm, nativeNorm))
        }
    }

    private fun String.normalizeImageAlt(): String =
        lines().joinToString("\n") { it.normalizeImageAltBrackets() }

    companion object {
        private val CORPUS_DIR = File(MARKDOWN_CORPUS_DIR)
        private const val TAG_JVM = "parity-jvm-tree"
        private const val TAG_NATIVE = "parity-native-tree"

        /**
         * Samples whose JVM-tree and native-tree renders STILL diverge after BUG #1 (heading-drop),
         * BUG #2 (inline-code-backtick), class [A] (heading inline-token trim-space) and class [B]
         * (backslash-escape resolution) were fixed. Each remaining sample is tagged with the residual
         * class [D]…[G] (or the parser-level [B-tilde] / [B-CJK]) documented in the class KDoc — these
         * are tree-/parser-level differences, NOT the renderer-shape-dispatch bugs T14.5 addressed, the
         * trim-space quirk [A], or the Text-leaf escape resolution [B]. They are SKIPPED (not failed,
         * not passed) so the suite is green WITHOUT pretending the native tree renders correctly — and
         * the rot guard re-promotes any that later reach parity. Samples that are NOW (or were always)
         * at hard parity are NOT here:
         *  - 01, 02, 07, 08, 09, 21, 26 — promoted to hard parity by the BUG #1/#2 fixes.
         *  - 11, 12, 17, 18, 25, 31 — promoted to hard parity by the class [A] heading trim-space fix.
         *  - 03 — promoted to hard parity by the class [B] backslash-escape fix (its only divergence
         *    was the `middle\_of\_a\_word` escape; both trees now render `middle_of_a_word`).
         *  - `20-html-block` — `hasHtmlBlocks`, both engines route to the identical `MarkdownNew`
         *    HTML path, so the tree is bypassed (trivially equal, but a real datapoint: the HTML
         *    arm is parity-safe).
         *  - `30-empty-and-whitespace` — whitespace-only input, both render nothing.
         */
        private val KNOWN_DIVERGENT_SAMPLES: Set<String> = setOf(
            // BUG #1 (native-heading-drop), BUG #2 (native-inline-code-backtick), class [A]
            // (heading inline-token trim-space), class [B] (backslash-escape resolution), class [C]
            // (list-item inline-run grouping), class [F] (ordered-list start number) and class [G]
            // (task-list `[x]` marker text) are ALL FIXED with shape-agnostic renderer dispatch in
            // Markdown.kt. Samples that diverged SOLELY on those have left this list:
            //   01, 02, 07, 08, 09, 21, 26 (BUG #1/#2),  11, 12, 17, 18, 25, 31 (class [A]),
            //   03-emphasis-nesting (class [B] — `middle\_of\_a\_word`),  10, 13 (class [C]),
            //   14-ordered-list-start (class [F]),  15-task-lists (class [G]).
            // 20-html-block / 30-empty were never listed.
            // The samples that REMAIN here diverge on the residual tree-/parser-level classes those
            // fixes UNMASKED (see class KDoc §"residual classes"). These are NOT renderer-shape-dispatch
            // bugs fixable without rewriting the native parser, so they stay skipped and cited. Tag
            // legend: [B] backslash-escape resolution, [C] inline-run grouping, [D] link/autolink/
            // footnote/reference text, [E] soft-break leading whitespace, [F] ordered-list start
            // number, [G] task-list `[x]` marker text.
            "04-links-inline",            // [D] email autolink `<x>` kept by JVM, resolved by native
            "05-links-with-titles",       // [D] reference link `[t][ref]` literal (JVM) vs resolved
            "06-images",                  // [D] inline-image run split differs between trees
            // 10-inline-code, 13-nested-lists: class [C] (list-item inline-run grouping) RESOLVED by
            // the shape-agnostic ListItemNode dispatch (Markdown.kt InlineRunMdNode) — both now render
            // at hard parity and have left this list.
            // class [A] (heading inline-token trim-space) is RESOLVED — the heading render path now
            // trims the assembled heading content ONCE at its outer boundaries instead of trimming
            // each leaf token (Markdown.kt: leaf arm no longer trims; AnnotatedString.trimEnds() in
            // Paragraph). 6 samples that diverged SOLELY on [A] now render at hard parity and have
            // left this list: 11, 12, 17, 18, 25, 31. The 6 samples below were ALSO tagged [A] but
            // diverge on a SECOND class that [A] was masking; their heading lines now match, so they
            // are re-tagged with the residual class proven at the tree-dump level (parity-rig first-diff
